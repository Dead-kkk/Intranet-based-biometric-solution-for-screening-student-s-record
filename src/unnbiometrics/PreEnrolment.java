    private void preSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_preSubmitActionPerformed
        // TODO add your handling code here:
        
        String regNo = preRegNo.getText();
        
        try {
            Identity id = Identity.load(regNo);
            if (id == null) {
                JOptionPane.showMessageDialog(null, "Student Not Verified");
            } else {
                id = new Identity(id.regNumber, 
                        id.fingerprint, 
                        image, id.dateRegistered);
                Database.helper(Identity.class)
                        .update(id);
            }
            JOptionPane.showMessageDialog(null, "Update Successfull!");
        } catch (IOException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Unable to update student!");
        }
    }//GEN-LAST:event_preSubmitActionPerformed

