    private void prePicUploadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_prePicUploadActionPerformed
        // TODO add your handling code here:
        JFileChooser chooser = new JFileChooser();
        chooser.showOpenDialog(null);
        FileNameExtensionFilter filter  = new FileNameExtensionFilter("*.images","jpg","png","gif");
        chooser.addChoosableFileFilter(filter);
        
        File f = chooser.getSelectedFile();
        String filename = f.getAbsolutePath();
        if (filename != null) {
           preImageIcon.setIcon(ResizeImage(filename));
        }
        else{
           return ;
          }
        // preImageIcon.setText(filename);

        try {
            image = ImageIO.read(new File(filename));
        } catch (IOException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_prePicUploadActionPerformed

    public ImageIcon ResizeImage(String imgPath) {
        ImageIcon myImage =  new ImageIcon(imgPath);
        Image img = myImage.getImage();
        Image newImage = img.getScaledInstance(
                preImageIcon.getWidth(), 
                preImageIcon.getHeight(), 
                Image.SCALE_SMOOTH);
        return new ImageIcon(newImage);
    }
    
        
