    private void fingerPrintBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_fingerPrintBtnActionPerformed

        String reg = this.regNo.getText();
        if (reg.isEmpty()) {
            JOptionPane.showMessageDialog(null, "check student's regNo");
            return;
        }

        try {
            EnrollmentForm form = new EnrollmentForm(this);
            form.setVisible(true);
            if (form.NrolSuccess(reg)) {
                fingerPrintLabel.setIcon(new ImageIcon(getClass().getResource("/unnbiometrics/res/biometrics_enrol_passed.png")));
                JOptionPane.showMessageDialog(null, "fingerprint enrolled successfully!");

            }
            else{
                fingerPrintLabel.setIcon(new ImageIcon(getClass().getResource("/unnbiometrics/res/biometrics_enrol_failed.png")));
                }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "FINGERPRINT ALREADY ENROLLED FOR THIS REGNO-"+reg, "ENROLLMENT FAILURE",JOptionPane.ERROR_MESSAGE);
             fingerPrintLabel.setIcon(new ImageIcon(getClass().getResource("/unnbiometrics/res/biometrics_enrol_failed.png")));
            Logger.getLogger(addStudentForm.class.getName()).log(Level.SEVERE, null, ex);
            
        }

    }//GEN-LAST:event_fingerPrintBtnActionPerformed

