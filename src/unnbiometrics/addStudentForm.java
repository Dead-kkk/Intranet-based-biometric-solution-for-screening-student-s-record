    public void EditStudent(Student s) {
        
        try {
            Department d = Database.helper(Department.class).get(s.department);
            facultyCombo.removeAllItems();
            facultyCombo.addItem(d.faculty);
        } catch (IOException ex) {
            Logger.getLogger(addStudentForm.class.getName()).log(Level.SEVERE, null, ex);
        }
//
        regNo.setText(s.regNumber);
        firstname.setText(s.name);
        lastName.setText(s.surname);
        department.removeAllItems();
        department.addItem(s.department);
        genderCombo.removeAllItems();
        genderCombo.addItem(s.gender.toString());
        level.removeAllItems();
        level.addItem(Integer.toString(s.level));
        DOB.setDate(s.birth);
        StdAdress.setText(s.address);
        stdPhoneNum.setText(s.phone);
        StdEmail.setText(s.email);

        SaveBtn.setEnabled(false);
    }


